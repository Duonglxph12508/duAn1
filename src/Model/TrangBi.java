
package Model;

/**
 *
 * @author Tran_Hang
 */
public class TrangBi {
    private String maTB, maPT;

    public TrangBi() {
    }

    public TrangBi(String maTB, String maPT) {
        this.maTB = maTB;
        this.maPT = maPT;
    }

    public String getMaTB() {
        return maTB;
    }

    public void setMaTB(String maTB) {
        this.maTB = maTB;
    }

    public String getMaPT() {
        return maPT;
    }

    public void setMaPT(String maPT) {
        this.maPT = maPT;
    }
    
    
}
